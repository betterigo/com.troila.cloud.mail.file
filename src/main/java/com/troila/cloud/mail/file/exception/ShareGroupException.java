package com.troila.cloud.mail.file.exception;

public class ShareGroupException extends Exception{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1031329043166144173L;

	public ShareGroupException() {
		super();
	}

	public ShareGroupException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public ShareGroupException(String message, Throwable cause) {
		super(message, cause);
	}

	public ShareGroupException(String message) {
		super(message);
	}

	public ShareGroupException(Throwable cause) {
		super(cause);
	}
	
}
