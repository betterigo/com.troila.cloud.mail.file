package com.troila.cloud.mail.file.exception;

public class UserSettingsException extends Exception{

	/**
	 * 
	 */
	private static final long serialVersionUID = -3175693737348864657L;

	public UserSettingsException() {
		super();
	}

	public UserSettingsException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

	public UserSettingsException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public UserSettingsException(String arg0) {
		super(arg0);
	}

	public UserSettingsException(Throwable arg0) {
		super(arg0);
	}

}
