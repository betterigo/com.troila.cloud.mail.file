package com.troila.cloud.mail.file.exception;

public class FolderException extends Exception{

	/**
	 * 
	 */
	private static final long serialVersionUID = 3163331772796851840L;

	public FolderException() {
		super();
	}

	public FolderException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public FolderException(String message, Throwable cause) {
		super(message, cause);
	}

	public FolderException(String message) {
		super(message);
	}

	public FolderException(Throwable cause) {
		super(cause);
	}
	
	
}
